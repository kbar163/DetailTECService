CREATE TABLE ROL
(
    ID_ROL INT NOT NULL PRIMARY KEY ,
    TIPO VARCHAR(20) NOT NULL ,

);

CREATE TABLE TIPOS_PAGO
(
    ID_PAGO INT NOT NULL PRIMARY KEY ,
    TIPO VARCHAR(20) NOT NULL ,

);

CREATE TABLE TRABAJADOR 
(
    CEDULA_TRABAJADOR VARCHAR(9) NOT NULL PRIMARY KEY ,
    FECHA_NACIMIENTO DATE NOT NULL ,
    NOMBRE VARCHAR(20) NOT NULL ,
    PRIMER_APELLIDO VARCHAR(20) NOT NULL ,
    SEGUNDO_APELLIDO VARCHAR(20) NOT NULL ,
    ID_ROL INT NOT NULL FOREIGN KEY REFERENCES ROL(ID_ROL) ,
    ID_PAGO INT NOT NULL FOREIGN KEY REFERENCES TIPOS_PAGO(ID_PAGO) ,
    FECHA_INGRESO DATE NOT NULL ,
    PASSWORD_TRABAJADOR VARCHAR(20) NOT NULL
);

CREATE TABLE SUCURSAL 
(
    NOMBRE VARCHAR(20) NOT NULL PRIMARY KEY ,
    TELEFONO VARCHAR(20) NOT NULL ,
    ID_EMPLEADO_GERENTE VARCHAR(9) NOT NULL FOREIGN KEY REFERENCES TRABAJADOR(CEDULA_TRABAJADOR) ,
    PROVINCIA VARCHAR(20) NOT NULL ,
    CANTON VARCHAR(20) NOT NULL ,
    DISTRITO VARCHAR(20) NOT NULL ,
    FECHA_APERTURA DATE NOT NULL ,
    FECHA_INICIO_GERENCIA DATE NOT NULL
);

CREATE TABLE PROVEEDOR 
(
    CEDULA_JURIDICA VARCHAR(20) NOT NULL PRIMARY KEY ,
    NOMBRE VARCHAR(20) NOT NULL ,
    CONTACTO VARCHAR(20) NOT NULL  ,
    PROVINCIA VARCHAR(20) NOT NULL ,
    CANTON VARCHAR(20) NOT NULL ,
    DISTRITO VARCHAR(20) NOT NULL ,
    CORREO_ELECTRONICO VARCHAR(20) NOT NULL
    
);

CREATE TABLE INSUMOS
(
    NOMBRE VARCHAR(20) NOT NULL PRIMARY KEY ,
    COSTO INT NOT NULL ,
    MARCA VARCHAR(20) NOT NULL ,
    CEDULA_PROVEEDOR VARCHAR(20) NOT NULL FOREIGN KEY REFERENCES PROVEEDOR(CEDULA_JURIDICA)

);